    const changeImage = (e) => {
      let index = 0;
      index = e.target.getAttribute("data-anim");
      console.log(index);

      for (let k = 0; k < allPhotos.length; k++) {
        if (allPhotos[k].getAttribute("data-anim") != index) {
          allPhotos[k].classList.remove("photo_meca--selected");
        } else {
          allPhotos[k].classList.add("photo_meca--selected");
        }
      }
    };

    const comment = (e) => {
      const btnCommentFilled = document.querySelector("#bubble-filled");
      const btnCommentEmpty = document.querySelector("#bubble-empty");
      const formComment = document.querySelector(".form__container");

      if (btnCommentEmpty.classList.contains("bubble--commented")) {
        btnCommentEmpty.classList.remove("bubble--commented");
        btnCommentFilled.classList.add("bubble--commented");
        formComment.classList.add("form--open");
      } else {
        btnCommentEmpty.classList.add("bubble--commented");
        btnCommentFilled.classList.remove("bubble--commented");
        formComment.classList.remove("form--open");
      }
    };

    btnComment.addEventListener("click", comment);
    allBtns.forEach((btn) => btn.addEventListener("click", changeContent));
