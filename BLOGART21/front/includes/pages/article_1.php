            <h2>Découvre mon histoire</h2>
            <p>
              Conçue en 2007 sous les aspirations de mes géniteurs danois BIG et
              français FREAKS, je vois finalement le jour le 28 juin 2018.
              Plusieurs complications ont compromis ma naissance et plongé dans
              l’incertitude mes futurs admirateurs. Impatients de découvrir mon
              physique et de me rencontrer, ils ont suivi de près ma conception,
              alimentant la toile d’articles à mon effigie.<br />
              Véritable star, je suis la naissance de l’année 2018 la plus
              attendue. À mon arrivée, mes formes singulières ne passent pas
              inaperçues et font le bonheur des bordelais. En effet, mon
              géniteur danois Bjarke Ingels, viennent apporter modernité,
              novation et espoir à un ancien abattoir symbolisant souffrance et
              cruauté. Concepteur de mes demi-frères danois - 8 House - et
              américain - VIA 57 West-, je suis la dernière fierté de la
              famille. Respectivement originaires de France et du Danemark, mes
              créateurs m’ont offert une double nationalité qui a fortement
              influencé ma personnalité. Il est vrai que j’aime me faire
              remarquer et casser les codes des standards de beauté fait partie
              de mes convictions. Mon physique atypique attire les convoitises
              de nombreux photographes, sûrement impressionnés par ma photogénie
              et la géométrie de mes formes.
            </p>
