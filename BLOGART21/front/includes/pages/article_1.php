            <h2>À quoi je ressemble ?</h2>
            <p>
              Assez extravertie, j’aime être sous le feu des projecteurs et je
              fais tout pour me faire remarquer. Placée à l’entrée de la ville
              au niveau du quai de Paludate, il est difficile de ne pas me voir.
              Que ce soit depuis la route, la Garonne, ou même les airs, peu
              importe le point de vue, les regards se tournent irréversiblement
              vers moi. Ainsi je porte énormément d’importance à mon apparence
              qui se doit d’être irréprochable. Au milieu de tant d’ancienneté,
              je brille de par ma jeunesse. Ma moitié se doit de répondre à mes
              exigences. Tout d’abord, être à la hauteur de mes 37 mètres de
              haut et 120 mètres de long, car oui, la taille compte ! Ensuite,
              comprendre la complexité des multiples facettes de ma personnalité
              qui, j’en suis sûre, te feront chavirer. Enfin, puisque je suis
              moi-même hors du commun grâce à mes formes uniques, j’aimerais
              retrouver ce côté atypique chez mon partenaire. J’aime relever les
              challenges et repousser mes limites. Chef-d'oeuvre d'ingénierie,
              mon imposante carrure défie les lois de la gravité et de la
              géométrie. Le mouvement de mes courbes révèle ma nature dynamique
              et c’est au travers de ces innombrables carreaux de verre que je
              rayonne. Si la montée de mes escaliers te séduit, je te mènerais
              goûter aux plaisirs ensoleillés que mon belvédère t’offrira.
            </p>
